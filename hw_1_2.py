print(bin(5), bin(6))  # Представление чисел в двоичном виде.
print(5 & 6)  # Побитное И, в двоичном виде 100, что равно числу 4
print(5 | 6)  # Побитное ИЛИ, в двоичном виде 111, что равно числу 7
print(5 ^ 6)  # Побитное исключающее ИЛИ, в двоичном виде 011, что равно числу 3
print(~ 5)
print(5 << 2)  # Свдиг влево на два знака 10100 = 20
print(5 >> 2)  # Свдиг вправо на два знака 1
